    private void clockwiseRotationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clockwiseRotationActionPerformed
        if(start==0)
        {
            CustomJPanel.clockwise=1;
            CustomJPanel.endTime=Long.MIN_VALUE;
            start=1;
        }
        else{
            CustomJPanel.clockwise=0;
            start=0;
        }

    }//GEN-LAST:event_clockwiseRotationActionPerformed

